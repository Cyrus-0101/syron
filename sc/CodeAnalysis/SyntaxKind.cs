namespace Syron

{
    // The SyntaxKind enum is used to represent the different kinds of tokens that the lexer can produce.
    enum SyntaxKind
    {
        NumberToken,
        WhiteSpaceToken,
        PlusToken,
        MinusToken,
        StarToken,
        SlashToken,
        OpenParenthesisToken,
        CloseParenthesisToken,
        EndOfFileToken,
        BadToken,
        NumberExpression,
        BinaryExpression,
        ParenthesizedExpression,
    }
}
